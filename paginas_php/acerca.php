<p>Fundación de la empresa Fecha: 27 de enero del 2025
Fundada por: Óscar Samuel Pérez de Gante
CDMX - Polanco - Antara Fashion Hall

Historia
Un día en el que Samuel y cuatro amigos más nos encontrábamos en una conferencia en el Centro Astronómico Clavius, nos reunimos en la terraza del observatorio. Al estar observando el cielo a través de los telescopios, surgió una conversación sobre cómo podríamos compartir nuestra pasión por la astronomía con más personas. La idea de crear una empresa en línea de productos astronómicos surgió natural y eventualmente.
¿Cómo surgió la empresa o plan de negocio?: Por el interés mutuo en el tema.
¿Qué razón nos motivó a asociarnos?: El querer promover la astronomía con más personas y ayudar a quienes ya se interesan en el tema a poder tener herramientas para disfrutar ese gusto y posiblemente aprender más.
